package mulekick

import (
	"fmt"
	"net/http"
	"time"
)

// gracefully stolen from gin-gonic/gin

var (
	green   = string([]byte{27, 91, 57, 55, 59, 52, 50, 109})
	white   = string([]byte{27, 91, 57, 48, 59, 52, 55, 109})
	yellow  = string([]byte{27, 91, 57, 55, 59, 52, 51, 109})
	red     = string([]byte{27, 91, 57, 55, 59, 52, 49, 109})
	blue    = string([]byte{27, 91, 57, 55, 59, 52, 52, 109})
	magenta = string([]byte{27, 91, 57, 55, 59, 52, 53, 109})
	cyan    = string([]byte{27, 91, 57, 55, 59, 52, 54, 109})
	reset   = string([]byte{27, 91, 48, 109})
)

func (w ResponseWriter) LogMiddleware(r *http.Request) {
	end := time.Now()
	latency := end.Sub(w.start)
	comment := ""

	fmt.Printf("%s %3d %s %v %13v |%s  %s %-7s %s\n%s",
		colorForStatus(w.statusCode), w.statusCode, reset,
		end.Format("15:04:05"),
		latency,
		colorForMethod(r.Method), reset, r.Method,
		r.URL.Path,
		comment,
	)
}

func colorForStatus(code int) string {
	switch {
	case code >= 200 && code < 300:
		return green
	case code >= 300 && code < 400:
		return white
	case code >= 400 && code < 500:
		return yellow
	default:
		return red
	}
}

func colorForMethod(method string) string {
	switch method {
	case "GET":
		return blue
	case "POST":
		return cyan
	case "PUT":
		return yellow
	case "DELETE":
		return red
	case "PATCH":
		return green
	case "HEAD":
		return magenta
	case "OPTIONS":
		return white
	default:
		return reset
	}
}
